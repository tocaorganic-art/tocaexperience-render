// Função auxiliar para normalizar texto (remover acentos e lowercase)
export function normalizeText(text) {
  if (!text) return "";
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

// Função para calcular score de relevância
export function calculateRelevance(event, searchTokens) {
  let score = 0;
  
  const normalizedName = normalizeText(event.name);
  const normalizedArtist = normalizeText(event.artist);
  const normalizedLocal = normalizeText(event.local);
  const normalizedRegion = normalizeText(event.region);
  const normalizedTags = event.tags.map(t => normalizeText(t));
  const normalizedDate = normalizeText(event.dateLabel);
  
  searchTokens.forEach(token => {
    // Match exato em nome = peso 10
    if (normalizedName.includes(token)) {
      score += 10;
    }
    
    // Match em artista = peso 8
    if (normalizedArtist && normalizedArtist.includes(token)) {
      score += 8;
    }
    
    // Match em região = peso 6
    if (normalizedRegion.includes(token)) {
      score += 6;
    }
    
    // Match em local = peso 5
    if (normalizedLocal.includes(token)) {
      score += 5;
    }
    
    // Match em data = peso 4
    if (normalizedDate.includes(token)) {
      score += 4;
    }
    
    // Match em tags = peso 3
    normalizedTags.forEach(tag => {
      if (tag.includes(token)) {
        score += 3;
      }
    });
  });
  
  return score;
}